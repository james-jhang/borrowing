package tw.taipei.gc.borrowing.adapter.item.restfulapi;


public class ItemViewModel {
    private String ID;
    private String name;

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getID() {
        return this.ID;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }
}